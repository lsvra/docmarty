import UIKit

extension UIColor {
    
    static var backgroundPrimary: UIColor {
        return UIColor(named: "BackgroundPrimary")!
    }
    
    static var backgroundSecondary: UIColor {
        return UIColor(named: "BackgroundSecondary")!
    }
    
    static var labelPrimary: UIColor {
        return UIColor(named: "LabelPrimary")!
    }
    
    static var labelSecondary: UIColor {
        return UIColor(named: "LabelSecondary")!
    }
    
    static var fillPrimary: UIColor {
        return UIColor(named: "FillPrimary")!
    }
}
